package io.libsoft.playball.model;

public class Position {

  private double x;
  private double y;


  public Position() {
    x = 0;
    y = 0;


  }

  public void setX(double x) {
    this.x = x;
  }

  public void setY(double y) {
    this.y = y;
  }

  public double getX() {
    return x;
  }

  public double getY() {
    return y;
  }
}
